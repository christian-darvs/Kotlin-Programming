fun main() {
    println(addNumbers(10,2))
    println(subtractNumbers(12, 3))
    println(multiplyNumbers(14, 3))
    println(divideNumbers(14,2))
    
}

fun addNumbers(num1: Int, num2: Int): Int {
    return num1 + num2
}

fun subtractNumbers(num1: Int, num2: Int): Int {
    return num1 - num2
}

fun multiplyNumbers(num1: Int, num2: Int): Int {
    return num1 - num2
}

fun divideNumbers(num1: Int, num2: Int): Int {
    return num1 / num2
}
